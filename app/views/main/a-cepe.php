        <p>A Companhia Editora de Pernambuco – Cepe é uma sociedade de economia mista que tem como atividade principal a edição e impressão do Diário Oficial do Estado de Pernambuco.</p>
        <p>Além disso, a edição de periódicos e produção de impressos em geral também contemplam sua lista de atividades. Bem como, a edição de livros que contam com a qualidade editorial, gráfica e de conteúdo e que vêm ganhando destaque nas estantes mundo afora.</p>
        <p>Atualmente, a Cepe também oferece gestão, guarda e digitalização de documentos com a Cepe Doc e a emissão de certificados digitais com a Cepe Digital.</p>

        <strong>NASCIMENTO DA CEPE</strong>

        <p>Com a Cepe já se vão mais de 50 anos de história e muitas publicações. Em 1967 surge a Companhia Editora de Pernambuco responsável pela publicação do Diário Oficial do Estado de Pernambuco, divulgando atos, decretos e regulamentos oficiais que devam ter execução no âmbito estadual. O D.O. também recebe e edita publicações legais de empresas privadas e organizações sociais.</p>

        <strong>TECNOLOGIA E MODERNIDADE</strong>

        <p>A Cepe tem compromisso com a sociedade e se empenha para ser um exemplo de modernidade e vanguarda. Não é a toa que o investimento em tecnologia e no aperfeiçoamento de rotinas produtivas estão presentes em suas ações.</p>

        <strong>VISÃO DE FUTURO</strong>

        <p>Ser referência regional no mercado editorial, gráfico, de gestão documental e digital até 2019.</p>

        <strong>MISSÃO</strong>

        <p>Publicar os atos oficiais e matérias legais, fazer gestão documental, serviços gráficos e editoriais com excelência e segurança, fomentando a cultura e a comunicação como contribuição à cidadania.</p>

