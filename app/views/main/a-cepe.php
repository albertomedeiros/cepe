        <strong>HISTÓRIA</strong>

        <p>A Cepe tem sua origem em 1924, ano da criação do Diário do Estado e da Repartição de Publicações Oficiais, responsáveis pela publicação do jornal oficial. Em 1926, por força de lei, a Repartição de Publicações Oficiais foi substituída pelo Departamento de Imprensa Oficial, que continuou a editar o Diário do Estado, nome substituído em 1944, quando passou a se chamar Diário Oficial.</p>
        <p>Ao Departamento de Imprensa Oficial sucedeu a atual Companhia Editora de Pernambuco – Cepe. Cabe ao Diário Oficial do Estado de Pernambuco (D.O.), sua principal publicação, a divulgação dos atos, decretos e regulamentos oficiais que devam ter execução no âmbito estadual. O D.O. também recebe e edita publicações legais de empresas privadas e organizações sociais.</p>

        <strong>EMPRESA MODERNA</strong>

        <p>Desde sua criação, a Cepe se empenha em ser exemplo de modernidade e vanguarda. O investimento em tecnologia e no aperfeiçoamento de rotinas produtivas sempre esteve presente em suas ações. Pioneira na publicação do Diário Oficial Eletrônico, a empresa disponibiliza para consulta universal e gratuita o acervo digital de todas as edições a partir de 1936. A tecnologia empregada permite a busca textual e por datas. O Diário Oficial pode ser acessado pelo site <a href="http://cepe.com.br" target="_blank">www.cepe.com.br</a>.</p>

        <strong>CEPE EDITORA</strong>

        <p>Com o objetivo de promover e difundir cultura em todo o Estado, a Cepe é responsável pela edição de periódicos, como o jornal literário Pernambuco. Criado em 1986, com o nome de Suplemento Cultural do Estado de Pernambuco, este material circula uma vez por mês com foco no mundo literário. Inicialmente encartado no Diário Oficial, hoje possui sua modalidade de assinatura individual.</p>
        <p>Outro produto de grande destaque é a revista Continente. São quase 20 anos consolidando sua proposta editorial que dialoga com o público mais amplo e agrega novos leitores através de matérias bem elaboradas e edição gráfica primorosa para quem não abre mão de cultura, arte e jornalismo.</p>
        <p>E ainda para promover o desenvolvimento cultural do Estado, a publicação de livros é mais uma iniciativa de destaque da Cepe. A criação do Conselho Editorial vêm rendendo bons resultados e elevando o nome da Cepe no cenário brasileiro com centenas de livros lançados com o selo da editora.</p>
        <p>A Cepe também conta com outras iniciativas como o Prêmio Cepe Nacional de Literatura, por exemplo; a criação da Coleção Acervo Pernambuco, que reedita livros fora de catálogo, inéditos ou raros, mas de grande importância para a cultura pernambucana; a constante modernização do seu parque gráfico e os títulos em formato e-book.</p>

        <strong>QUALIDADE DE IMPRESSÃO</strong>

        <p>Para aqueles que procuram seus serviços de impressão e acabamento a Cepe dispõe de uma equipe especializada para dar orientações quanto ao melhor papel, formatação e quantidade de páginas, além de emitir os orçamentos de acordo com as preferências do cliente. Com amplas instalações, moderno parque industrial e quadro de pessoal de reconhecida capacidade técnica, a gráfica da Cepe se insere entre as que produzem com maior rapidez e melhor qualidade.</p>

        <strong>DIGITALIZAÇÃO</strong>

        <p>O setor de digitalização foi estruturado para atender à crescente demanda por conversão de arquivos físicos em digitais. Foram realizados investimentos na aquisição de modernos scanners para digitalização de documentos de todos os tipos e formatos. Documentos administrativos e históricos são digitalizados e organizados para consulta, trazendo agilidade e eficiência aos processos internos de empresas privadas e instituições públicas.</p>

        <strong>GUARDA, GESTÃO E DIGITALIZAÇÃO DE DOCUMENTOS</strong>

        <p>A Cepe é responsável pela guarda, gestão e digitalização de documentos intermediários das Secretarias e Órgãos de Pernambuco, segundo Lei no 15.529, 23 de junho de 2015, que garante a exclusividade para realizar este serviço em todo o Estado. </p>
        <p>Os documentos podem ser geridos, armazenados e digitalizados de acordo com a necessidade de cada cliente, garantindo a proteção de arquivos administrativos e históricos, além de proporcionar mais agilidade e eficiência aos processos das instituições públicas e empresas privadas.</p>


        <strong>CERTIFICAÇÃO DIGITAL</strong>

        <p>O Certificado Digital é um documento eletrônico que contém todos os dados sobre a pessoa e/ou empresa que o utiliza com o objetivo de comprovar sua autenticidade. Ele permite transações mais seguras, evitando fraudes e falsificações, já que permite a troca de documentos com autenticação, sigilo e integridade de conteúdo, sem a necessidade de converter documentos em papel para assiná-los.</p>
        <p>Atualmente a Cepe é devidamente credenciada pela ICP-Brasil (Infraestrutura de Chaves Públicas Brasileira) para realizar a certificação digital em todo o país.</p>

        <strong>ATENDIMENTO AO PÚBLICO</strong>

        <p>Seja atendido na nossa loja, localizada no andar térreo do prédio da Cepe, e adquira exemplares da revista Continente ou do jornal literário Pernambuco, solicite a publicação de matérias legais, anúncios, adquira livros, contrate serviços gráficos e emita certificados digitais. A Cepe também mantém a Museu&Cia, ponto de venda de suas edições, localizado nas dependências do Centro Cultural Cais do Sertão (Av. Alfredo Lisboa, s/n – Recife Antigo).</p>

