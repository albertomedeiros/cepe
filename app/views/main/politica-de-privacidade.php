        <p><strong>REGISTRO DE UTILIZADORES E ÁREA RESERVADA</strong></p>

        <p>O registro de utilizadores permite o acesso a vários serviços do site www.cepe.com.br tais como o Diário Oficial Eletrônico e a Loja Virtual;</p>

        <p>O pedido de subscrição requer a introdução de uma palavra-chave e um endereço de correio eletrônico, para onde é enviado um link que permitirá validar e confirmar o registro efetuado. Esta palavra-chave garante ao utilizador o acesso à gestão dos serviços na Área reservada (designada “A Minha Área”) do site www.cepe.com.br.</p>

        <p>Os dados fornecidos pelos utilizadores serão apenas utilizados na realização dos serviços selecionados.</p>

        <p><strong>PALAVRA-CHAVE</strong></p>

        <p>A área reservada é protegida por uma palavra-chave (senha), que será indicada pelo utilizador no momento do registro. No caso de o utilizador esquecer a palavra-chave, poderá recuperá-la, indicando o e-mail registrado/associado.</p>

        <p>Recomendação: Uma boa palavra-chave deve incluir letras, números e caracteres especiais, não devendo ser uma palavra comum ou facilmente detectável.</p>

        <p>A palavra-chave é pessoal e intransmissível; o uso que o utilizador lhe dá é da sua total responsabilidade.</p>

        <p><strong>OUTROS SERVIÇOS</strong></p>

        <p>Outros serviços que possam surgir posteriormente e que requeiram a introdução de dados pessoais e/ou das respectivas entidades estão abrangidos pela política de privacidade e segurança do site www.cepe.com.br referida neste documento.</p>

        <p>SEGURANÇA E QUALIDADE DA INFORMAÇÃO</p>

        <p>É nosso objetivo garantir a qualidade e integridade da informação fornecida pelos utilizadores do nosso site, tendo para isso implementado as medidas necessárias, tanto em nível tecnológico como organizacional, de forma a manter a informação segura, precisa, atualizada e completa.</p>

        <p>Nas áreas do portal onde são recolhidos os dados pessoais dos utilizadores, a sua transmissão é empreitada, usando o protocolo SSL 3.0 (Secure Sockets Layer), com chaves de encriptação.</p>

        <p>Utilizando esta tecnologia, os dados consultados no portal e os dados enviados pelo utilizador para o portal são encriptados utilizando uma chave de 128 bits, que é atualmente a forma de encriptação mais segura suportada pelos atuais navegadores de internet.</p>


        <p><strong>PARTILHA DE INFORMAÇÕES PESSOAL</strong></p>

        <p>As informações pessoais recolhidas no portal são usadas exclusivamente pela Companhia Editora de Pernambuco – Cepe para os fins indicados em cada caso.</p>

        <p><strong>LINKS</strong></p>

        <p>O nosso site poderá conter links para outros sites. A Cepe não se responsabiliza pelas políticas de privacidade ou conteúdo desses sites.</p>

        <p><strong>ARQUIVOS DE REGISTRO ESTATÍSTICO</strong></p>

        <p>São registrados os endereços IP de todas as ligações ao site www.cepe.com.br. Esta informação será utilizada para efetuar análises estatísticas agregadas, não sendo estabelecida qualquer relação com os serviços e interações com os utilizadores.</p>

        <p>A Companhia Editora de Pernambuco monitorizará toda a informação estatística relativa à utilização do portal de forma agregada e anônima. Os dados utilizados para este fim não contêm dados de identificação pessoal ou informação privada.</p>

        <p>As análises efetuadas a partir da informação estatística agregada serão efetuadas para interpretar os padrões de utilização do site e melhorar, de forma contínua, os níveis de serviço e a satisfação do utilizador. A informação estatística agregada resultante das análises efetuadas poderá ser revelada a terceiros ou publicamente.</p>

