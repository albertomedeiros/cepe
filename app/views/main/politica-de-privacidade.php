        <p><strong>PRIVACIDADE E SEGURANÇA</strong></p>

        <p>A Companhia Editora de Pernambuco – Cepe, entidade responsável pelo site www.cepe.com.br, encara seriamente a privacidade e a proteção dos dados registrados pelos seus utilizadores.</p>

        <p>A Cepe assegura aos seus utilizadores privacidade e segurança nos dados facultados para os vários serviços disponibilizados, sendo apenas pedidos e recolhidos os dados necessários à prestação do serviço de acordo com as indicações explícitas no site e as opções do utilizador. Caso o requeira, o titular dos dados tem o direito de obter o acesso, retificação ou a eliminação dos dados facultados</p>

        <p>Em seguida, apresentamos as práticas de tratamento de informação que adotamos para www.cepe.com.br, incluindo a forma como a informação é recolhida, como é usada e com quem é partilhada.</p>

        <p>Assumimos para com os utilizadores do site www.cepe.com.br os seguintes compromissos:</p>

        <p>
          Proceder ao tratamento de dados de forma lícita e leal, recolhendo apenas a informação necessária e pertinente à finalidade a que se destina;<br />
          Permitir ao titular dos dados o acesso e correção das informações sobre ele registradas, transmitindo-as em linguagem clara e rigorosamente correspondente ao conteúdo do registro;<br />
          Não utilizar os dados recolhidos para finalidade incompatível com a do seu objetivo;<br />
          Manter os dados exatos e, se necessário, atuais;<br />
          Assegurar o consentimento expresso do titular dos dados sempre que tal for exigido;<br />
          Garantir gratuitamente o direito de eliminação dos dados utilizados quando requerido pelo titular;<br />
          Ter sistemas de segurança que impeçam a consulta, modificação, destruição ou adição dos dados por pessoa não autorizada a fazê-lo;<br />
          Respeitar o sigilo profissional em relação aos dados tratados;<br />
          Não realizar interconexão de dados pessoais, salvo com autorização legal.</p>

        <p><strong>DADOS PESSOAIS</strong></p>

        <p>Em geral, o site www.cepe.com.br pode ser visitado sem que seja necessário revelar qualquer informação pessoal. Existem, no entanto, áreas do mesmo onde necessitamos que os utilizadores forneçam os seus dados pessoais, de forma a usufruir dos serviços disponibilizados.</p>

        <p>Os serviços www.cepe.com.br que necessitam de introdução de dados pessoais ou referentes às respectivas entidades, por iniciativa ou demonstração de interesse dos utilizadores, são:</p>

        <p>
          – Registro para área de acesso à Loja Virtual;<br />
          – Registro para utilização do D.O.E.<br />
        </p>

        <p>A inserção de dados de identificação do utilizador será efetuada através do preenchimento de um formulário de registro online e ocorrerá de acordo com as mais estritas regras de segurança.</p>

        <p>Os dados recolhidos através do site www.cepe.com.br são introduzidos no sistema informático que os trata, sendo processados automaticamente, destinando-se os dados à gestão de serviços do portal.</p>

        <p>Todos os colaboradores da entidade responsável estão abrangidos por uma obrigação de confidencialidade referente aos dados a que tenham acesso no âmbito das operações da respectiva base informática, estando devidamente informados da importância do cumprimento desse dever legal de sigilo, sendo responsáveis pelo cumprimento dessa obrigação de confidencialidade.</p>

        <p>A Cepe garante ao utilizador registrado a possibilidade de aceder aos seus dados pessoais através da sua página pessoal e proceder à sua atualização e correção, através da opção “Dados Pessoais”.</p>

