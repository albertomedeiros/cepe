        <p><strong>Critérios para recebimento e apreciação de originais pelo Conselho Editorial</strong></p>

        <p>I - Os originais de livros submetidos à Cepe, exceto aqueles que a Diretoria considera projetos da própria Editora, são analisados pelo Conselho Editorial, que delibera a partir dos seguintes critérios:</p>

        <p>1. Contribuição relevante à cultura.</p>

        <p>2. Sintonia com a linha editorial da Cepe, que privilegia:</p>

        <p>a) A edição de obras inéditas, em português, com relevância cultural nos vários campos do conhecimento, suscetíveis de serem apreciadas pelo leitor e que preencham os seguintes requisitos: originalidade, correção, coerência e criatividade;</p>

        <p>b) A reedição de livros técnicos de qualquer área ou obras literárias de qualquer gênero,consideradas fundamentais para o patrimônio cultural;</p>

        <p>3. O Conselho pode acolher tese ou dissertações, desde que estas contemplem a ampliação do universo de leitores, visando a democratização do conhecimento e que sejam feitas as modificações necessárias à edição.</p>

        <p>II - Atendidos tais critérios, o Conselho emitirá parecer sobre o projeto analisado, que será comunicado ao proponente, cabendo à diretoria da Cepe decidir sobre a publicação.</p>

        <p>III - Os textos devem ser entregues em duas vias, em papel A4, conforme a nova ortografia, em fonte Times New Roman, tamanho 12, com espaço de1,5, sem rasuras e contendo, quando for o caso, índices e bibliografias apresentados conforme as normas técnicas em vigor.</p>

        <p>IV - Serão rejeitados textos que atentem contra a Declaração dos Direitos Humanos e fomentem a violência e as diversas formas de preconceito.</p>

        <p>V - As cópias devem ser encaminhadas à Presidência da Cepe, para o endereço indicado a seguir, sob registro de correio ou protocolo, acompanhados de correspondência do autor, na qual informará seu currículo resumido e endereço para contato.</p>

        <p>VI - As cópias apresentadas para análise não serão devolvidas.</p>

