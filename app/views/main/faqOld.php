          <h2>12. Baixou o arquivo P7S e não sabe como usá-lo e nem para que ele serve?</h2>
        </div>
        <div class="conteudo-faq">
          <p>R. Arquivos P7S são arquivos de texto associados à PKCS #7 Signature, que é um tipo de criptografia utilizada para assinar documentos e certificados, em cartórios ou órgãos do governo, por exemplo. A forma mais rápida e simples de abrir este tipo de arquivo é dar um duplo clique no mesmo. Isto permitirá que a inteligência do seu Sistema Operacional decida o aplicativo correto a ser usado. No caso de seu arquivo P7S não abrir, existe uma grande possibilidade de que você não possua o aplicativo software correto instalado em seu computador para visualizar ou editar este tipo de extensão. Caso seu computador abra o arquivo P7S, com um aplicativo incompatível com o mesmo, você precisará modificar suas configurações de associação de arquivo. Caso ainda esteja encontrando dificuldades em abrir este tipo de arquivo, basta instalar um dos aplicativos abaixo listados:</p>

          <p>
            - File View Pro<br />
            - P7S Viewer<br />
            - Assinador Digital
          </p>
